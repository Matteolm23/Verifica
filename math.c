int somma (int n1, int n2)
{

    int risultato = n1 + n2;
    return risultato ;

}

int diff (int n1, int n2)
{

    int risultato = n1 - n2;
    return risultato;

}

int molt (int n1, int n2)
{

    int risultato = n1 * n2;
    return risultato;

}

int div (int n1, int n2)
{

    int risultato = n1 / n2;
    return risultato;

}